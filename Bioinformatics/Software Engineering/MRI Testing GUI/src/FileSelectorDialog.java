	public Imagen2D[] getImages() throws IOException {
		// TODO Auto-generated method stub

		Imagen2D[] imagenes = new Imagen2D[0];
		int numeroImagenes = 1;
		File reading = reading = new File(this.jFileChooser1.getCurrentDirectory()+ "/" + this.jTextField2.getText() + numeroImagenes + this.jTextField1.getText());
		while(reading.exists()){
			System.out.println(this.jFileChooser1.getCurrentDirectory()+"/" + this.jTextField2.getText() + numeroImagenes + this.jTextField1.getText());
			imagenes = Arrays.copyOf(imagenes, imagenes.length+1);
			imagenes[numeroImagenes-1] = new Imagen2D("---", "---", "---", ImageIO.read(reading));
			numeroImagenes++;
			reading = new File(this.jFileChooser1.getCurrentDirectory()+ "/" + this.jTextField2.getText() + numeroImagenes + this.jTextField1.getText());
			System.out.println("file: " + reading);
		}
		return imagenes;
	}
