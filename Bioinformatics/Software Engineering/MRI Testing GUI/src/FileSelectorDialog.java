    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed

    	try {
			this.superWindow.imagenes = this.getImages();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    	this.superWindow.depth.setEnabled(true);
    	this.superWindow.selectColor.setEnabled(true);
    	this.superWindow.viewInput.setEnabled(true);
    	this.superWindow.zoomValue.setEnabled(true);
    	this.superWindow.renderizable = new Imagen3D(this.superWindow.imagenes);
    	this.superWindow.renderizable.slice(1, 0, null);
    	this.superWindow.visualizerWindow.setImage(this.superWindow.renderizable.imagenRenderizable);
    	this.superWindow.depth.setMaximum(this.superWindow.renderizable.slides);
    	this.superWindow.depth.setMinimum(1);
    	
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

